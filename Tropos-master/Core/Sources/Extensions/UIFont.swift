import UIKit

extension UIFont {
    public static func defaultLightFont(size size: CGFloat) -> UIFont {
        return UIFont(name: "DINNextLTPro-Light", size: size)!
    }

    public static func defaultRegularFont(size size: CGFloat) -> UIFont {
        return UIFont(name: "DINNextLTPro-Regular", size: size)!
    }
}
